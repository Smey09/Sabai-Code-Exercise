// ! <Type>value
var someValue = "Hello, TypeScript!";
// Casting using angle bracket syntax
var strValue = someValue;
console.log(strValue); // Output: "Hello, TypeScript!"
//! value as Type
var some_Value = "Hello, TypeScript!";
// Casting using 'as' syntax
var str_Length = some_Value.length;
console.log(str_Length); // Output: 18
